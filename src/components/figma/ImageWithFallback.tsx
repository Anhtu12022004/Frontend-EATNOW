// Ảnh fallback từ mock data (Unsplash - không bị CORS)
const FALLBACK_BRANCH_IMAGE = 'https://images.unsplash.com/photo-1669131196140-49591336b13e?crop=entropy&cs=tinysrgb&fit=max&fm=jpg&ixid=M3w3Nzg4Nzd8MHwxfHNlYXJjaHwxfHxyZXN0YXVyYW50JTIwaW50ZXJpb3IlMjBtb2Rlcm58ZW58MXx8fHwxNzYwMzczOTI3fDA&ixlib=rb-4.1.0&q=80&w=1080';

interface ImageWithFallbackProps extends React.ImgHTMLAttributes<HTMLImageElement> {
  fallbackSrc?: string;
}

export function ImageWithFallback(props: ImageWithFallbackProps) {
  const [currentSrc, setCurrentSrc] = useState(props.src);
  const [didError, setDidError] = useState(false)

  const handleError = () => {
    // Nếu chưa dùng fallback, thử dùng fallback image
    if (currentSrc !== (props.fallbackSrc || FALLBACK_BRANCH_IMAGE)) {
      setCurrentSrc(props.fallbackSrc || FALLBACK_BRANCH_IMAGE);
    } else {
      // Nếu fallback cũng lỗi, hiển thị error placeholder
      setDidError(true);
    }
  }

  const { src, alt, style, className, fallbackSrc, ...rest } = props

  return didError ? (
    <div
      className={`inline-block bg-gray-100 text-center align-middle ${className ?? ''}`}
      style={style}
    >
      <div className="flex items-center justify-center w-full h-full">
        <img src={ERROR_IMG_SRC} alt="Error loading image" {...rest} data-original-url={src} />
      </div>
    </div>
  ) : (
    <img src={currentSrc} alt={alt} className={className} style={style} {...rest} onError={handleError} />
  )
}
